public class SimpleSpriteDef {
    private String texture;
    private float u;
    private float v;
    private float u2;
    private float v2;

    public String getTexture() {
        return texture;
    }

    public float getU() {
        return u;
    }

    public float getV() {
        return v;
    }

    public float getU2() {
        return u2;
    }

    public float getV2() {
        return v2;
    }
}
